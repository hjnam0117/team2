package bookstore;

public class rank {
	private int bookid;
	private int krank;
	private int arank;
	private int prank;
	private int irank;
	private int yrank;
	public rank() {
		super();
	}
	public int getBookid() {
		return bookid;
	}
	public void setBookid(int bookid) {
		this.bookid = bookid;
	}
	public int getKrank() {
		return krank;
	}
	public void setKrank(int krank) {
		this.krank = krank;
	}
	public int getArank() {
		return arank;
	}
	public void setArank(int arank) {
		this.arank = arank;
	}
	public int getPrank() {
		return prank;
	}
	public void setPrank(int prank) {
		this.prank = prank;
	}
	public int getIrank() {
		return irank;
	}
	public void setIrank(int irank) {
		this.irank = irank;
	}
	public int getYrank() {
		return yrank;
	}
	public void setYrank(int yrank) {
		this.yrank = yrank;
	}
}
